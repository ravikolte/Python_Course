# Python code for variable declaration (Note: Python is dynamically typed, so you don't explicitly specify the data type)
integer_value = 0    # Declare an integer variable
double_value = 0.0   # Declare a double-precision floating-point variable
string_value = "Test"    # Declare a string variable
char_value = 'a'     # Declare a character variable (Note: Python doesn't have a distinct character type)
bool_value = True    # Declare a boolean variable

print("Integer = ", integer_value)
print("Double = ", double_value)
print("String = ", string_value)
print("Characters = ", char_value)
print("Boolean = ", bool_value)

#In Python, you don't explicitly declare the data type, as it is dynamically typed.
